#!/usr/bin/env python3
"""Generate tool-specific custom agents from shared definitions."""

from __future__ import annotations

import argparse
import json
import sys
import textwrap
from pathlib import Path


REQUIRED_FIELDS = {
    "name",
    "description",
    "effort",
    "codex-model",
    "codex-sandbox",
}

OPTIONAL_FIELDS = {
    "claude-name",
    "codex-name",
    "claude-tools",
    "claude-model",
    "claude-hooks",
}

CLAUDE_HOOKS = {"readonly-bash"}
CODEX_SANDBOXES = {"read-only", "workspace-write"}
REASONING_EFFORTS = {"low", "medium", "high", "xhigh", "max", "ultra"}

GENERATED_MARKER = "# Generated by agents/bin/generate-agent-configs.py; do not edit."

def parse_agent(path: Path) -> tuple[dict[str, str], str]:
    text = path.read_text(encoding="utf-8")
    parts = text.split("---", 2)
    if len(parts) != 3 or parts[0].strip():
        raise ValueError(f"{path}: expected YAML frontmatter")

    lines = parts[1].strip("\n").splitlines()
    metadata: dict[str, str] = {}
    index = 0
    while index < len(lines):
        line = lines[index]
        if not line or line[0].isspace() or ":" not in line:
            index += 1
            continue

        key, raw_value = line.split(":", 1)
        value = raw_value.strip()
        if value in {">", ">-", "|", "|-"}:
            folded: list[str] = []
            index += 1
            while index < len(lines) and (
                not lines[index] or lines[index][0].isspace()
            ):
                folded.append(lines[index].strip())
                index += 1
            metadata[key] = " ".join(part for part in folded if part)
            continue

        metadata[key] = value
        index += 1

    missing = REQUIRED_FIELDS - metadata.keys()
    if missing:
        raise ValueError(f"{path}: missing frontmatter fields: {sorted(missing)}")
    unknown = metadata.keys() - REQUIRED_FIELDS - OPTIONAL_FIELDS
    if unknown:
        raise ValueError(f"{path}: unknown frontmatter fields: {sorted(unknown)}")
    if metadata["name"] != path.stem:
        raise ValueError(f"{path}: name must match the lowercase filename")
    if metadata["codex-sandbox"] not in CODEX_SANDBOXES:
        raise ValueError(f"{path}: unsupported codex-sandbox")
    if metadata["effort"] not in REASONING_EFFORTS:
        raise ValueError(f"{path}: unsupported effort")
    if not metadata["codex-model"]:
        raise ValueError(f"{path}: codex-model must not be empty")
    if (
        "claude-hooks" in metadata
        and metadata["claude-hooks"] not in CLAUDE_HOOKS
    ):
        raise ValueError(f"{path}: unsupported claude-hooks")

    instructions = parts[2].strip() + "\n"
    return metadata, instructions


def toml_string(value: str) -> str:
    return json.dumps(value, ensure_ascii=False)


def toml_multiline_literal(value: str) -> str:
    if "'''" in value:
        raise ValueError("TOML multiline literal cannot contain three apostrophes")
    return "'''\n" + value.rstrip("\n") + "\n'''"


def render_codex_agent(source: Path) -> tuple[str, str]:
    metadata, instructions = parse_agent(source)
    name = metadata.get("codex-name", metadata["name"])
    filename = f"{name}.toml"
    rendered = "\n".join(
        [
            GENERATED_MARKER,
            f"name = {toml_string(name)}",
            "description = "
            + toml_multiline_literal(textwrap.fill(metadata["description"], width=88)),
            f"model = {toml_string(metadata['codex-model'])}",
            f"model_reasoning_effort = {toml_string(metadata['effort'])}",
            f"sandbox_mode = {toml_string(metadata['codex-sandbox'])}",
            "developer_instructions = " + toml_multiline_literal(instructions),
            "",
        ]
    )
    return filename, rendered


def render_claude_agent(source: Path) -> tuple[str, str]:
    metadata, instructions = parse_agent(source)
    name = metadata.get("claude-name", metadata["name"])
    frontmatter = [
        "---",
        f"name: {name}",
        "description: >-",
        *(
            f"  {line}"
            for line in textwrap.wrap(metadata["description"], width=76)
        ),
    ]
    if "claude-tools" in metadata:
        frontmatter.append(f"tools: {metadata['claude-tools']}")
    if "claude-model" in metadata:
        frontmatter.append(f"model: {metadata['claude-model']}")
    frontmatter.append(f"effort: {metadata['effort']}")
    if metadata.get("claude-hooks") == "readonly-bash":
        frontmatter.extend(
            [
                "hooks:",
                "  PreToolUse:",
                "    - matcher: Bash",
                "      hooks:",
                "        - type: command",
                '          command: bash "$HOME/.claude/hooks/readonly-guard.sh"',
                "          timeout: 10",
            ]
        )
    frontmatter.extend(["---", "", instructions])
    return f"{name}.md", "\n".join(frontmatter)


def expected_outputs(source_dir: Path, target: str) -> dict[str, str]:
    renderer = render_claude_agent if target == "claude" else render_codex_agent
    outputs: dict[str, str] = {}
    for path in sorted(source_dir.glob("*.md")):
        filename, content = renderer(path)
        if filename in outputs:
            raise ValueError(f"duplicate generated filename: {filename}")
        outputs[filename] = content
    return outputs


def check_outputs(output_dir: Path, expected: dict[str, str], pattern: str) -> bool:
    stale: list[str] = []
    for filename, content in expected.items():
        target = output_dir / filename
        if not target.is_file() or target.read_text(encoding="utf-8") != content:
            stale.append(filename)

    unexpected = [
        path.name
        for path in output_dir.glob(pattern)
        if path.name not in expected
    ]
    if stale or unexpected:
        if stale:
            print(
                f"{output_dir}: stale or missing: {', '.join(stale)}",
                file=sys.stderr,
            )
        if unexpected:
            print(
                f"{output_dir}: obsolete generated files: {', '.join(unexpected)}",
                file=sys.stderr,
            )
        return False
    return True


def write_outputs(output_dir: Path, expected: dict[str, str], pattern: str) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    for filename, content in expected.items():
        (output_dir / filename).write_text(content, encoding="utf-8")

    for path in output_dir.glob(pattern):
        if path.name not in expected:
            path.unlink()


def main() -> int:
    home_dir = Path(__file__).resolve().parents[2]
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true")
    parser.add_argument(
        "--source", type=Path, default=home_dir / "agents" / "definitions"
    )
    parser.add_argument("--target", choices=("claude", "codex"), required=True)
    parser.add_argument("--output", type=Path, required=True)
    args = parser.parse_args()

    outputs = expected_outputs(args.source, args.target)
    if not outputs:
        raise SystemExit(f"no agent definitions found in {args.source}")
    pattern = "*.md" if args.target == "claude" else "*.toml"
    if args.check:
        return 0 if check_outputs(args.output, outputs, pattern) else 1

    write_outputs(args.output, outputs, pattern)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
